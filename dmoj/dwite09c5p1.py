for i in range(5):
    n = int(input())
    
    if n == -5:
        print("*****|-----")
    elif n == -4:
        print("-****|-----")
    elif n == -3:
        print("--***|-----")
    elif n == -2:
        print("---**|-----")
    elif n == -1:
        print("----*|-----")
    elif n == 0:
        print("-----|-----")
    elif n == 1:
        print("-----|*----")
    elif n == 2:
        print("-----|**---")
    elif n == 3:
        print("-----|***--")
    elif n == 4:
        print("-----|****-")
    elif n == 5:
        print("-----|*****")